<h3>Do I need to have an account on any Online Judge to use MOJ?</h3>

<p>No, however, we encourage you to do one so you can fill up your profile!</p>

<br/>

<h3>How do I set my online judges accounts?</h3>

<p>Go to your profile, click on edit profile. There you will find multiple fields with the respective judges.</p>

<br/>

<h3>How do I verify my Online Judges accounts?</h3>

<p>Once you set your username for the respective judge, visit your profile. You will find a 4 character key,
 you should add this to your firstname field in your online judge account. Remember that this is only a one time
 validation, so you can delete the key from your firstname once we have accepted your account.</p>

<h3>My UVA account is not beign validated. What's going on?</h3>

<p>UVA sometimes takes a while to register changes on your profile, just leave the key there for a while and try refreshing again.</p>

<br/>

<h3>My profile is not loading at all. What should I do?</h3>

<p>Any profile crash should be reported immediately at <a href="mailto:feedback@moj.projectace.net">feedback@moj.projectace.net</a>!</p>

<br/>

<h3>What is Problem Equivalence?</h3>

<p>Problem Equivalance is our way of knowing if two problems between different judges are the same or similar. This is used to 
get a fair MOJ score. You can report a problem equivalence <a href="probequiv">here</a> and vote for your favorite <a href="probequiv/browse">here</a>.</p>

<br/>

<h3>My Score is not correct. What's wrong?</h3>

<p>The scoring system in MOJ is still a work in progress and some minor bugs may come up, we thank you for your patience.</p>

